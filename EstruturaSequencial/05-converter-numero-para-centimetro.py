"""
Faça um Programa que converta metros para centímetros.

Para efetuar a conversão de metros em centímetros é necessários você multiplicar por 100

1 metro = 100 centímetros
"""

print("Conversao de metros para centimetros")

metros = float(input("Digite o valor em metros: "))

centimetros = metros * 100;

print ("O valor %.2f metros equivale a %.2f centimetros" %(metros, centimetros))
